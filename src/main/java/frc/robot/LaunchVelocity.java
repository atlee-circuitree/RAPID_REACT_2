package frc.robot;

public class LaunchVelocity {

    public int topMotorVelocity;
      
    public int bottomMotorVelocity;
  
    public LaunchVelocity(int topMotorVelocity, int bottomMotorVelocity) {
  
      this.topMotorVelocity = topMotorVelocity;
      this.bottomMotorVelocity = bottomMotorVelocity;
  
    }
      
}